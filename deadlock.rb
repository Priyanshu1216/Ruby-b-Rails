# Deadlock

Deadlock is a situation in concurrent programming when two or more threads or processes are unable to preoceed because
each one is waiting for the other to release a resource or take a specific ation.
As a result, the processes or threads remain in the blocked state indefinitely causing the system to freeze or become irreponsive.


